// Igual (==)
// Retorna verdadeiro caso os operandos sejam iguais. 3 == var1
"3" == var1
3 == '3'

// Não iguais (!=)
// Retorna verdadeiro caso os operandos não sejam iguais. var1 != 4
var2 != 3

// Extritamente igual (===)
// Retorna verdadeiro caso os operandos sejam iguais e do mesmo tipo. Veja também Object.is e igualdade
3 === var1

// Extritamente não igual (!==)
// Retona verdadeiro caso os operandos não sejam iguais e/ou não sejam do mesmo tipo
var1 !== "3"
3 !== '3'

// Maior que (>)
// Retorna verdadeiro caso o operando da esquerda seja maior que o da direita
var2 > var1
"12" > 2

// Maior ou igual (>=)
// Retorna verdadeiro caso o operando da esquerda seja maior ou igual que o da direita
var2 >= var1
var1 >= 3

// Maior que (<)
// Retorna verdadeiro caso o operando da esquerda seja menor que o da direita
var1 < var2
"2" < "12"

// Maior ou igual (<=)
// Retorna verdadeiro caso o operando da esquerda seja menor ou igual que o da direita
var1 <= var2
var2 <= 5