// Módulo (%)
// Operador binário. Retorna o inteiro restante da divisão de dois operandos.

12 % 5 // retorna 2.

// ##########################################################################

// Incremento (++) e Decremento (--)
++x
x++

const a = ++2; // 3 - incrementa antes
const b = 2++; // 2 - incrementa depois

--x
x--

// ##########################################################################

// Negação (-) e Adição (+)

-3
+"3"    // retorna 3
+true   // retorna 1
+false  // retorna 0
-true   // retorna -1

// ##########################################################################

// Operador de exponenciação

2 ** 3   // retorna 8
10 ** -1 // retorna 0.1

// ##########################################################################

// Operador de agrupamento
2 * (3 + 2)